<?php
//Като използвате цикъл for, да се напише програма, която отпечатва всички числа 
//в интервала $x - $y, които се делят на 3 или на 7.
  $x=12;
  $y=1;
  if ($x>$y) {
    $x+=$y;
    $y=$x-$y;
    $x-=$y;
  }
  for ($i=$x; $i<=$y; $i++) {
    if ($i % 3 == 0 || $i %7 == 0) {
      echo "$i ";
    }
  }
